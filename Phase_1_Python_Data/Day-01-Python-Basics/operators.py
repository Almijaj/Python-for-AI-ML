# operators=+,-,*,/
print(5+6)
print(5-6)
print(5*6)
print(5/6)
# floor division (//) this is when we have to get the value in natural form
print(5//6)
# modulus operator(%) it is used to get reminder 
print(5%6)
# exponential (**) this is used when we have to get the multiple of any number or cube root of any number
print(2**6)